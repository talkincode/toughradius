package org.toughradius.common.bits;

/**
 * 创建一个OFFSET,用于Bits偏移
 */
public class OffSet
{
	private int off;
    
    public OffSet(int off)
    {
    	this.off = off;
    }
    
    public void setOff(int off)
    {
    	this.off = off;
    }
    
    public int getOff()
    {
    	return off;
    }
}
